.global getMatrix
; r24: index for the matrix
; return in r24 the value
; change r24, r30, r31
getMatrix:
    mov r30, r24
    ldi r31, 0
    subi r30, lo8(-(matrix))
    sbci r31, hi8(-(matrix))
    ld r24, Z
    ret

.global setMatrix
; r24: index, r22: data to set  (init from main.S)
; change r30, r31
setMatrix:
	mov r30, r24		;r30 <- index
	ldi r31, 0
	subi r30, lo8(-(matrix))
	sbci r31, hi8(-(matrix))
	st Z, r22
	ret


.global copy
; (r24,r25): the address of the matrix to copy from
; (r22,r23): the address of the matrix to copy to
; change r18, r21, r28, r29, r30, r31
copy:
	ldi r21, 0                     ; r21 loop index

	mov r30, r24
	mov r31, r25
	mov r28, r22
	mov r29, r23
	copyLoop:
		ld r18, Z+
		st Y+, r18

		inc r21                   ; loop handling
		cpi r21, 16               ;
		brlt copyLoop             ;
	ret

.global print
;prints (r24,r25)
print:
	ldi r20, 0                     ; r20 loop index

	mov r30, r24
	mov r31, r25
	printLoop:
		mov r24, r20

		ld r24, Z+
		call USART_send

		inc r20			             ; loop handling
		cpi r20, 16                  ;
		brlt printLoop               ;
	ret


.global transpose
; (r24,r25): the address of the matrix to transpose
; change r20, r21, r22, r23, r28, r29, r30, r31
transpose:
    mov r30, r24
    mov r31, r25
    mov r28, r24
    mov r29, r25
    ldi r22, 0              ;i
    lineLoop:
        ldi r23, 0          ;j
        columnLoop:
            ; set Z to the first case
            ldi r21, 4      ;(i*4)
            mul r22, r21
            mov r21, r0
            push r30
            push r31
            add r30, r23
            adc r31, 0      ;??? add with carry 0
            add r30, r21
            adc r31, 0      ;??? add with carry 0

            ;set Y to the other case
            ldi r21, 4      ;(j*4)
            mul r23, r21
            mov r21, r0
            push r28
            push r29
            add r28, r22
            adc r29, 0      ;??? add with carry 0
            add r28, r21
            adc r29, 0      ;??? add with carry 0

            ld r20, Y       ;load from Y to r20
            ; swap r20 and data from Z
            ld r19, Z       
            st Z, r20
            st Y, r19

            pop r29
            pop r28
            pop r31
            pop r30
            inc r23         ;j+=1
            cp r23, r22
            brlt columnLoop
        inc r22             ;i+=1
        cpi r22, 4
        brlt lineLoop
    ret



